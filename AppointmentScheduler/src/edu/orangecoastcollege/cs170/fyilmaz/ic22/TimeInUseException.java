package edu.orangecoastcollege.cs170.fyilmaz.ic22;

public class TimeInUseException extends Exception {

	public TimeInUseException() {
		
	}

	public TimeInUseException(String arg0) {
		super(arg0);
		
	}

	public TimeInUseException(Throwable arg0) {
		super(arg0);
		
	}

	public TimeInUseException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public TimeInUseException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}

